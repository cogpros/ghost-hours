#!/usr/bin/env python3
"""refresh-dataset.py -- Regenerate the shipped dataset and README stats from one artifact.

Runs the share export, validates it, ships it to dataset/, and rewrites the
README stats block and dataset paragraph FROM THE EXPORT FILE ITSELF -- one
source artifact, so the README numbers and the shipped data cannot drift.

Exits nonzero (and touches nothing) on any validation failure.

Usage:
  refresh-dataset.py            # refresh dataset/ + README in the repo root
  refresh-dataset.py --dry-run  # validate and print, write nothing

Copyright 2026 Raven Systems Inc. Licensed under Apache 2.0.
"""

import json
import re
import subprocess
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
REPO_ROOT = SCRIPT_DIR.parent
README = REPO_ROOT / "README.md"
DATASET_DIR = REPO_ROOT / "dataset"

FORBIDDEN_FIELDS = {"desc", "note", "fwr_note", "project", "tags", "ts", "session_id"}

# The shipped dataset uses a human-readable pseudonym, not the local random
# participant UUID. The UUID stays on the machine; only the label ships.
PUBLISH_LABEL = "ANON-001"

STATS_START = "<!-- stats:generated by scripts/refresh-dataset.py — edit the script, not this block -->"
STATS_END = "<!-- stats:end -->"
DATASET_START = "<!-- dataset-line:generated by scripts/refresh-dataset.py -->"
DATASET_END = "<!-- dataset-line:end -->"


def fail(msg):
    print(f"REFUSED: {msg}", file=sys.stderr)
    sys.exit(1)


def run_export():
    result = subprocess.run(
        ["bash", str(SCRIPT_DIR / "ghost-hours-share.sh"), "--yes"],
        capture_output=True, text=True,
    )
    if result.returncode != 0:
        fail(f"share export failed: {result.stderr.strip()[:400]}")
    m = re.search(r"Export saved: (.+)", result.stdout)
    if not m:
        fail("share export produced no 'Export saved:' line")
    return Path(m.group(1).strip())


def validate(export):
    for key in ("dataset", "schema_version", "participant_id", "export_date",
                "session_count", "entries"):
        if key not in export:
            fail(f"export missing top-level key: {key}")
    if not export["entries"]:
        fail("export has zero entries")
    for i, e in enumerate(export["entries"]):
        leaked = FORBIDDEN_FIELDS & set(e.keys())
        if leaked:
            fail(f"entry {i} leaks forbidden field(s): {sorted(leaked)}")
    prior = sorted(DATASET_DIR.glob("ghost-hours-export-*.json"))
    if prior:
        with open(prior[-1]) as f:
            prev_count = len(json.load(f).get("entries", []))
        if len(export["entries"]) < prev_count:
            fail(f"entry count went backwards: {len(export['entries'])} < {prev_count} "
                 f"in {prior[-1].name} (append-only log should not shrink)")
    return True


def render_stats_block(export):
    entries = export["entries"]
    sessions = [e for e in entries if e.get("type") in ("speed", "unlock")]
    speed = [e for e in sessions if e["type"] == "speed"]
    unlock = [e for e in sessions if e["type"] == "unlock"]
    dates = sorted(e["date"] for e in entries if e.get("date"))
    from datetime import date as ddate
    d0 = ddate.fromisoformat(dates[0])
    d1 = ddate.fromisoformat(dates[-1])
    days = (d1 - d0).days + 1

    hh = sum(e.get("human_mins", 0) for e in sessions) / 60
    gh = sum(e.get("gh_mins", 0) for e in sessions) / 60
    cr = gh / hh if hh else 0

    backlog = [e["backlog_months"] for e in sessions if e.get("backlog_months")]
    fwc = [e["fwc"] for e in sessions if e.get("fwc")]
    fwc_avg = sum(fwc) / len(fwc) if fwc else 0

    interactive = sum(1 for e in sessions if e.get("entry_class") == "human")
    automated = len(sessions) - interactive

    dist = {i: sum(1 for v in fwc if v == i) for i in range(1, 11)}
    max_count = max(dist.values()) if fwc else 1
    hist = "\n".join(
        f"  {i:>2}:  {'#' * max(1, round(dist[i] / max_count * 10)) if dist[i] else ''}  ({dist[i]})"
        for i in range(1, 11)
    )

    pid = export["participant_id"]
    return days, f"""```
=== Participant: {pid} | {days} days ===

Total sessions:        {len(sessions):,}
  Speed:               {len(speed):,}
  Unlock:              {len(unlock):,}
Hugr Hours invested:   {hh:,.0f}h  ({hh/8:,.1f} work-days)
Ghost Hours conjured:  {gh:,.0f}h ({gh/8:,.1f} work-days)
Overall Conjure Rate:  {cr:.1f}x

Backlog cleared:       {sum(backlog)/12:,.1f} years (self-reported, {len(backlog)} sessions)
Felt Weight avg:       {fwc_avg:.1f} / 10 ({len(fwc)} scored)

FW-C Distribution:
{hist}

Sources: {interactive} interactive, {automated} scheduled/automated.
```

Real data. One person, using Ghost Hours daily for {days} days. No descriptions, no notes, no project names. Just the math and the felt weight."""


def render_dataset_line(export, filename):
    entries = export["entries"]
    sessions = sum(1 for e in entries if e.get("type") in ("speed", "unlock"))
    notes = sum(1 for e in entries if e.get("type") == "methodology-note")
    return (f"The de-identified export ships in this repo at "
            f"[`dataset/{filename}`](dataset/{filename}): {len(entries)} records "
            f"({sessions} sessions + {notes} methodology notes) from "
            f"{export['participant_id']}, as of {export['export_date']}.")


def splice(text, start, end, replacement):
    if start not in text or end not in text:
        fail(f"README markers missing: {start}")
    pre, rest = text.split(start, 1)
    _, post = rest.split(end, 1)
    return f"{pre}{start}\n{replacement}\n{end}{post}"


def main():
    dry = "--dry-run" in sys.argv
    export_path = run_export()
    with open(export_path) as f:
        export = json.load(f)
    export["participant_id"] = PUBLISH_LABEL
    for e in export["entries"]:
        if "participant_id" in e:
            e["participant_id"] = PUBLISH_LABEL
    validate(export)

    filename = f"ghost-hours-export-{export['export_date']}.json"
    days, stats_block = render_stats_block(export)
    dataset_line = render_dataset_line(export, filename)

    if dry:
        print(stats_block)
        print(dataset_line)
        print("DRY RUN: validated, nothing written.")
        return

    readme = README.read_text()
    readme = splice(readme, STATS_START, STATS_END, stats_block)
    readme = splice(readme, DATASET_START, DATASET_END, dataset_line)

    for old in DATASET_DIR.glob("ghost-hours-export-*.json"):
        old.unlink()
    DATASET_DIR.mkdir(exist_ok=True)
    with open(DATASET_DIR / filename, "w") as dst:
        dst.write(json.dumps(export, indent=2, ensure_ascii=False))
    README.write_text(readme)
    print(f"Refreshed: dataset/{filename} ({len(export['entries'])} records), "
          f"README stats + dataset line ({days} days).")


if __name__ == "__main__":
    main()
